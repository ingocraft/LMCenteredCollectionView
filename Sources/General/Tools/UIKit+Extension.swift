import UIKit

extension UIScreen {
    class var mainScreenBounds: CGRect {
        return UIScreen.main.bounds
    }
    class var mainScreenWidth: CGFloat {
        return UIScreen.main.bounds.width
    }
    class var mainScreenHeight: CGFloat {
        return UIScreen.main.bounds.height
    }
}

